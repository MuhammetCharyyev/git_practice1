package day03_EscapeSequences;

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Java \nPython \nC#");

        System.out.println("--------------------------");

        System.out.println("Hello Sydeo \nHow are you all today? \nIt's nice to see you all! \nWhat class do we have next week?");

        System.out.println("--------------------------");

        System.out.println("\tJava is cool programming Language");

        System.out.println("--------------------------");

        System.out.println("/ \\");

        System.out.println("--------------------------");

        System.out.println("My favorite book is \"The Master and Margarita\"");

    }

}

/*
escape sequences: must be given with double quote
\n: start new line
\t: tab function or paragraph
\\: backslash, if I need 4 backslashes I have to put 8 backslashes
\": double quote

 */