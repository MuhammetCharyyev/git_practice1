package day09_IfStatements2;

public class MaxMin {

    public static void main(String[] args) {

        int num1 = 100, num2=200;

        if (num1 >num2){
            System.out.println(num1 + " is max num");
        }else{
            System.out.println(num2 + " is max num");
        }

        if (num1<num2){
            System.out.println(num1 + " is min num");
        }else{
            System.out.println(num2 + " is min num");
        }


    }
}
