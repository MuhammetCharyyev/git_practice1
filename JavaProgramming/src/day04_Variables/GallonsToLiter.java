package day04_Variables;

public class GallonsToLiter {

    public static void main(String[] args) {
        int gallon = 5;
        double conversionFactor = 3.79;
        double liter = gallon * conversionFactor;

        System.out.println("gallon = " + gallon);
        System.out.println("conversionFactor = " + conversionFactor);
        System.out.println("liter = " + liter);

    }

}

